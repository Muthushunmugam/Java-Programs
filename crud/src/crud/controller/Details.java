package crud.controller;

public class Details {

		
		private String id;
		private String name;
		private String dept;
		
		public String getsId() {
			return id;
		}
		public void setsId(String id) {
			this.id=id;
		}
		
		public String getsName() {
			return name;
		}
		public void setsName(String name) {
			this.name=name;
		}
		
		public String getsDept() {
			return dept;
		}
		public void setsDept(String dept) {
			this.dept=dept;
		}
		
		
	}


